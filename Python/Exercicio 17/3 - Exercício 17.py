dia = (input("Digite o número correspondente ao dia da semana(1- Domingo, 2- Segunda, etc.): "))
print()

if(dia == "1"):
    print(f"Domingo\n")
elif(dia == "2"):
    print(f"Segunda-feira\n")
elif(dia == "3"):
    print(f"Terça-feira\n")
elif(dia == "4"):
    print(f"Quarta-feira\n")
elif(dia == "5"):
    print(f"Quinta-feira\n")
elif(dia == "6"):
    print(f"Sexta-feira\n")
elif(dia == "7"):
    print(f"Sábado\n")
else:
   print(f"Valor invalido!\n")